package CollectionsConcepts;

public class Employee {

	// Global variables
	String name;
	int age;
	String orgination;
	int id;

	// Creating a Contractor for Employee Class
	public Employee(String name, int age, String orgination, int id) {
		this.name = name;
		this.age = age;
		this.orgination = orgination;
		this.id = id;

	}

}
